#include <stdio.h>

int main(void)
{
    printf("In sodden trenches I have heard men speak,\n");
    printf("Though numb and wretched, wise and witty things;\n");
    printf("And loved them for the stubbornness that clings\n");
    printf("Longest to laughter when Death's pulleys creak;\n");
    printf("And seeing cool nurses move on tireless feet\n");
    printf("To do abominable things with grace,\n");
    printf("Deemed them sweet sisters in that haunted place\n");
    printf("Where, with child's voices, strong men howl or bleat.\n");

    return 0;
}